package org.example.lesson2.homework2;

public class Car {

    //Поля
    String brand;
    String color;
    int price;
    int year;
    int speed;

    //Пустой конструктор
    public Car() {}

    //Заполенный конструктор
    public Car(String brand, String color, int price, int year, int speed) {

        this.brand = brand;
        this.color = color;
        this.price = price;
        this.year = year;
        this.speed = speed;



    }

}
